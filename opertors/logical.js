// || or opertor

// isme dono true true hota hai ek true or ek false bi true hota hai
// dono false false hota hai

const check3 = 4 > 3 || 10 > 5;
console.log(check3);
// true or true = true

const check4 = 4 > 3 || 10 < 5;
console.log(check4);
// true or false = true

const check5 = 4 < 3 || 10 < 5;
console.log(check5);
// false false = false

